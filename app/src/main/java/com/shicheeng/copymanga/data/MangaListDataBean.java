package com.shicheeng.copymanga.data;

public class MangaListDataBean {
    private String mangaListCoverUrl;
    private String mangaTitle;
    private String mangaAuthorText;

    public String getMangaListCoverUrl() {
        return mangaListCoverUrl;
    }

    public void setMangaListCoverUrl(String mangaListCoverUrl) {
        this.mangaListCoverUrl = mangaListCoverUrl;
    }

    public String getMangaTitle() {
        return mangaTitle;
    }

    public void setMangaTitle(String mangaTitle) {
        this.mangaTitle = mangaTitle;
    }

    public String getMangaAuthorText() {
        return mangaAuthorText;
    }

    public void setMangaAuthorText(String mangaAuthorText) {
        this.mangaAuthorText = mangaAuthorText;
    }
}
