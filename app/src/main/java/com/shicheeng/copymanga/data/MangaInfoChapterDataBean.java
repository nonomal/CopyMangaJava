package com.shicheeng.copymanga.data;

public class MangaInfoChapterDataBean {
    private String chapterTitle;
    private String chapterTime;
    private String uuidText;
    private String pathWord;

    public String getChapterTitle() {
        return chapterTitle;
    }

    public void setChapterTitle(String chapterTitle) {
        this.chapterTitle = chapterTitle;
    }

    public String getChapterTime() {
        return chapterTime;
    }

    public void setChapterTime(String chapterTime) {
        this.chapterTime = chapterTime;
    }

    public String getUuidText() {
        return uuidText;
    }

    public void setUuidText(String uuidText) {
        this.uuidText = uuidText;
    }

    public String getPathWord() {
        return pathWord;
    }

    public void setPathWord(String pathWord) {
        this.pathWord = pathWord;
    }
}
